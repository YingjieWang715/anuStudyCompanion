package au.edu.anu.comp6442.group03.studyapp.treevisualizer;

public class CoordPair {
    private final float x;
    private final float y;

    public CoordPair(float x, float y) {
        this.x = x;
        this.y = y;
    }

    public float getX(){
        return this.x;
    }

    public float getY(){
        return this.y;
    }

}
